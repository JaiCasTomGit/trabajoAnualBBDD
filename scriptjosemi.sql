DROP TABLE JUGADOR CASCADE CONSTRAINTS;
DROP TABLE CLASE CASCADE CONSTRAINTS;

--TABLA CLASE
CREATE TABLE CLASE (
    codigoClase NUMBER(3) PRIMARY KEY,
    nomClase VARCHAR2(20)
);

--TABLA JUGADOR
CREATE TABLE JUGADOR (
    codigoJugador NUMBER(3) PRIMARY KEY,
    nombre VARCHAR2(40),
    nivel NUMBER(2),
    codigoClase NUMBER(3),
    CONSTRAINT fk_jugador_clase FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

--TABLA ESTADISTICAS_CLASE
CREATE TABLE ESTADISTICAS_CLASE (
    codigoClase NUMBER(3) PRIMARY KEY,
    fuerza_base NUMBER(3),
    destreza_base NUMBER(3),
    arcano_base NUMBER(3),
    agilidad_base NUMBER(3),
    salud_base NUMBER(3),
    CONSTRAINT fk_estadisticas_clase_clase
        FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

--TABLA CLASE_GUERRERO
CREATE TABLE CLASE_GUERRERO (
    codigoClase NUMBER(3) PRIMARY KEY,
    CONSTRAINT fk_guerrero_clase
        FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

--TABLA CLASE_MAGO
CREATE TABLE CLASE_MAGO (
    codigoClase NUMBER(3) PRIMARY KEY,
    CONSTRAINT fk_mago_clase
        FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

--TABLA CLASE_CABALLERO
CREATE TABLE CLASE_CABALLERO (
    codigoClase NUMBER(3) PRIMARY KEY,
    CONSTRAINT fk_caballero_clase
        FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

--TABLA EST_JUGADOR
CREATE TABLE EST_JUGADOR (
    codigoJugador NUMBER(3) PRIMARY KEY,
    fuerza NUMBER(3),
    destreza NUMBER(3),
    arcano NUMBER(3),
    agilidad NUMBER(3),
    salud NUMBER(3),
    puntos_disponibles NUMBER(3),
    CONSTRAINT fk_est_jugador
        FOREIGN KEY (codigoJugador)
        REFERENCES JUGADOR(codigoJugador)
);

--TABLA MAPA
CREATE TABLE MAPA (
    codigoMapa NUMBER(3) PRIMARY KEY
);

--TABLA ENEMIGO
CREATE TABLE ENEMIGO (
    codigoEnemigo NUMBER(3) PRIMARY KEY
);

--TABLA INVENTARIO
CREATE TABLE INVENTARIO (
    codigoInventario NUMBER(3) PRIMARY KEY,
    tipoInventario VARCHAR2(50),
    codigoJugador NUMBER(3),
    codigoMapa NUMBER(3),
    CONSTRAINT fk_inventario_jugador 
        FOREIGN KEY (codigoJugador)
        REFERENCES JUGADOR(codigoJugador),
    CONSTRAINT fk_inventario_mapa 
        FOREIGN KEY (codigoMapa)
        REFERENCES MAPA(codigoMapa)
);

--TABLA OBJETO
CREATE TABLE OBJETO (
    codObjeto NUMBER(3) PRIMARY KEY,
    codigoInventario NUMBER(3),
    nomObjeto VARCHAR2(100),
    tipoObjeto VARCHAR2(50),
    CONSTRAINT fk_objeto_inventario 
        FOREIGN KEY (codigoInventario)
        REFERENCES INVENTARIO(codigoInventario)
);

--TABLA ARMA
CREATE TABLE ARMA (
    codigoArma NUMBER(3) PRIMARY KEY,
    codigoInventario NUMBER(3),
    codigoObjeto NUMBER(3),
    dano NUMBER(3),
    CONSTRAINT fk_arma_inventario 
        FOREIGN KEY (codigoInventario)
        REFERENCES INVENTARIO(codigoInventario),
    CONSTRAINT fk_arma_objeto 
        FOREIGN KEY (codigoObjeto)
        REFERENCES OBJETO(codObjeto)
);

--TABLA COPIAS_ARMAS
CREATE TABLE COPIAS_ARMAS (
    codigoArma NUMBER(3) PRIMARY KEY,
    numCopias NUMBER(3),
    CONSTRAINT fk_copias_armas 
        FOREIGN KEY (codigoArma)
        REFERENCES ARMA(codigoArma)
);

--TABLA ENMBASICO
CREATE TABLE ENMBASICO (
    codigoEnemigoBas NUMBER(3) PRIMARY KEY,
    codigoEnemigo NUMBER(3),
    CONSTRAINT fk_enmbasico_enemigo 
        FOREIGN KEY (codigoEnemigo)
        REFERENCES ENEMIGO(codigoEnemigo)
);

--TABLA JEFE
CREATE TABLE JEFE (
    codigoJefe NUMBER(3) PRIMARY KEY,
    nombreJefe VARCHAR2(100),
    codigoEnemigo NUMBER(3),
    CONSTRAINT fk_jefe_enemigo 
        FOREIGN KEY (codigoEnemigo)
        REFERENCES ENEMIGO(codigoEnemigo)
);

