-- TABLAS PRINCIPALES

CREATE TABLE CLASE (
    codigoClase NUMBER PRIMARY KEY,
    nomClase VARCHAR2(100) NOT NULL
);

CREATE TABLE JUGADOR (
    codigoJugador NUMBER PRIMARY KEY,
    nombre VARCHAR2(100) NOT NULL,
    nivel NUMBER,
    codigoClase NUMBER,
    CONSTRAINT fk_jugador_clase 
        FOREIGN KEY (codigoClase)
        REFERENCES CLASE(codigoClase)
);

CREATE TABLE MAPA (
    codigoMapa NUMBER PRIMARY KEY
);

CREATE TABLE ENEMIGO (
    codigoEnemigo NUMBER PRIMARY KEY
);

-- INVENTARIO Y OBJETOS

CREATE TABLE INVENTARIO (
    codigoInventario NUMBER PRIMARY KEY,
    tipoInventario VARCHAR2(50),
    codigoJugador NUMBER,
    codigoMapa NUMBER,
    CONSTRAINT fk_inventario_jugador 
        FOREIGN KEY (codigoJugador)
        REFERENCES JUGADOR(codigoJugador),
    CONSTRAINT fk_inventario_mapa 
        FOREIGN KEY (codigoMapa)
        REFERENCES MAPA(codigoMapa)
);

CREATE TABLE OBJETO (
    codObjeto NUMBER PRIMARY KEY,
    codigoInventario NUMBER,
    nomObjeto VARCHAR2(100),
    tipoObjeto VARCHAR2(50),
    CONSTRAINT fk_objeto_inventario 
        FOREIGN KEY (codigoInventario)
        REFERENCES INVENTARIO(codigoInventario)
);

CREATE TABLE ARMA (
    codigoArma NUMBER PRIMARY KEY,
    codigoInventario NUMBER,
    codigoObjeto NUMBER,
    dano NUMBER,
    CONSTRAINT fk_arma_inventario 
        FOREIGN KEY (codigoInventario)
        REFERENCES INVENTARIO(codigoInventario),
    CONSTRAINT fk_arma_objeto 
        FOREIGN KEY (codigoObjeto)
        REFERENCES OBJETO(codObjeto)
);

CREATE TABLE COPIAS_ARMAS (
    codigoArma NUMBER PRIMARY KEY,
    numCopias NUMBER,
    CONSTRAINT fk_copias_armas 
        FOREIGN KEY (codigoArma)
        REFERENCES ARMA(codigoArma)
);

CREATE TABLE ENMBASICO (
    codigoEnemigoBas NUMBER PRIMARY KEY,
    codigoEnemigo NUMBER,
    CONSTRAINT fk_enmbasico_enemigo 
    );
    
    
